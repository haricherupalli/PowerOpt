void SetTrie::insert(vector <int>& set)
{
  int i = 0;
  Node * curr_Node = top;
  while ( i < set.size())
  {
    if (curr_Node->find_child(set[i])) // ALREADY EXISTS
    {
      curr_Node = curr_Node->get_next(set[i]);
      i++;
    }
    else
    {
      curr_Node->insert_elems (set, i);
      break;
    }
  }
  collection.push_back(make_pair(0.0, set));
}

bool Node::essr(vector <int>& set, int i, bool strict)
{
  int from = set[i];
  int upto = set[i+1];
  bool found = false;
  map<int, Node*>::iterator it_from, it_upto;

  #ifdef debug
  cout << "At Node : " << element << "-->  {";
  for (map <int, Node*>::iterator it = children.begin(); it != children.end() ; it++) {
   cout << it->first << ", " ;
  }
  cout << "} and strict is " << strict <<  endl;
  #endif

  if (i == -1)
  {
    from = -1;
  }

  #ifdef debug
  cout << " From is : " << from << " Upto is : " << upto << endl;
  #endif
  if (i+1 == (int)set.size() && (strict | children.size() != 0)) {
    return true ;
  }
  it_from = children.lower_bound(from);
  it_upto = children.upper_bound(upto);
  map <int, Node*> range_map (it_from, it_upto);
  if (range_map.size() == 0) return false;
  for (map<int, Node*>::iterator it = range_map.begin(); it != range_map.end(); it++)
  {
    if (found) break;
    if (it->first == upto) {
      found = it->second->essr(set, i+1, strict);
    }
    else {
      found = it->second->essr(set, i, true);
    }
  }
  return found;
}


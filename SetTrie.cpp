bool Node::find_child (int child)
{
    map<int, Node* > ::iterator it = children.find(child);
    return (it != children.end()) ;
}

void Node::insert_elems(vector<int>& set, int i)
{
    if (i < set.size())
    {
      Node * nd = new Node;
      int elem = set[i];
      nd->element = elem;
      children.insert(pair < int , Node*> (elem, nd));
      nd->insert_elems(set, i+1);
    }
}

